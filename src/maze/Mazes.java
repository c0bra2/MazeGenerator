    private void generateMazeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateMazeButtonActionPerformed
        // make a palette instance and store characters to use for walls and paths
        Palette myPalette = new Palette();
        myPalette.setWall(wallTextField.getText());
        myPalette.setPath(pathTextField.getText());
        myPalette.setStart(startTextField.getText());
        myPalette.setExit(exitTextField.getText());
        
        //create a maze instance to work with
        myMaze = new Generator(Integer.parseInt(heightTextField.getText()),Integer.parseInt(widthTextField.getText()), myPalette);
        

        mazeTextArea.setText(myMaze.toString());
    }//GEN-LAST:event_generateMazeButtonActionPerformed

